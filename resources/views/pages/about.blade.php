        <h2 class="section-title">Historical Evolution</h2>
        <p class="section-content mb-4">The university's journey began in 1999 as the School of Engineering and Technology under Bharathidasan University. It soon became the Bharathidasan Institute of Technology (BIT). In 2007, it was renamed Anna University Tiruchirappalli and is now known as the University College of Engineering (BIT Campus). Despite changes, "BIT Campus" remains a cherished identity.</p>

        <h2 class="section-title">Campus and Infrastructure</h2>
        <p class="section-content mb-4">Set on a 354-acre expanse in southern Tiruchirappalli, the campus includes ten departments, six hostels, an indoor stadium, gym, health center, banks, guest houses, a canteen, and more. It pioneered Civil and Mechanical Engineering in Tamil, fostering education in the regional language.</p>

        <h2 class="section-title">Vision and Mission</h2>
        <p class="section-content mb-4">Our vision is to transform students into professionals and responsible citizens by disseminating knowledge tailored to societal needs. We strive to deliver quality education, foster innovation, promote collaborations, and uphold ethics and entrepreneurship.</p>

        <h2 class="section-title">Academic Programs and Departments</h2>
        <p class="section-content mb-4">Offering UG, PG, and doctoral programs, the university covers diverse disciplines like Civil, Mechanical, CSE, ECE, EEE, Biotechnology, and more. Postgraduate studies include Environmental and Software Engineering, Nano Science, and more. The MBCBS enhances faculty development through credit banking.</p>

        <h2 class="section-title">Rankings and Recognitions</h2>
        <p class="section-content mb-4">Anna University Tiruchirappalli ranks 383rd globally (QS 2025), 10th in India, 20th in NIRF 2024, and 14th in Outlook-ICARE MBA Rankings. The BIT campus ranked 93rd in IIRF 2023 and 18th in EduRank India.</p>

        <h2 class="section-title">Research Excellence</h2>
        <p class="section-content mb-4">The campus houses several autonomous research centers tackling climate change, energy, water management, and more. It supports 11,000+ Ph.D. scholars and awards 50 fellowships yearly through the Anna Centenary Research Corpus.</p>

        <h2 class="section-title">Placements and Industry Interface</h2>
        <p class="section-content mb-4">Top recruiters like Tata Power, Cognizant, Dell, Siemens, Microsoft, Infosys, and TCS partner with us. B.E./B.Tech students earn up to ₹7.5 LPA and MBA graduates up to ₹8 LPA, reflecting strong industry alignment and employability.</p>

